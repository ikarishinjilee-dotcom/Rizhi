function mockCategorySnapshot(categoryId: string, subCategoryId?: string): TransactionRecord["categorySnapshot"] {
  const category = mockCategories.find((item) => item.id === categoryId);
  const subCategory = subCategoryId ? mockCategories.find((item) => item.id === subCategoryId) : undefined;
  if (!category && !subCategory) return undefined;
  return {
    categoryName: category?.name ?? "未分类",
    subCategoryName: subCategory?.name,
  };
}

function mockBusinessType(transaction: TransactionRecord): TransactionRecord["businessType"] {
  if (transaction.type === "asset_purchase") return "asset_purchase";
  if (transaction.categoryId === "tx-asset-transfer") return "asset_transfer";
  if (transaction.categoryId === "tx-transfer") return "account_transfer";
  if (transaction.type === "repayment" || transaction.categoryId === "tx-repayment") return "debt_repayment";
  if (transaction.type === "refund") return "refund";
  if (transaction.addonId) return "asset_addon";
  if (transaction.partEventId) return "legacy_part_event";
  return "normal";
}

